namespace SocialPlus.Server.Managers
{
    /// <summary>
    /// defines a particular image size
    /// </summary>
    public struct ImageSize
    {
        /// <summary>
        /// One character symbol for this image size
        /// </summary>
        public char Id;

        /// <summary>
        /// Width of the image in pixels
        /// </summary>
        public int Width;
    }
}